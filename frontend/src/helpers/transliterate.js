export const transliterate = (word) => {
  const keys = {
    а: 'a',
    б: 'b',
    в: 'v',
    г: 'g',
    д: 'd',
    е: 'e',
    ё: 'e',
    ж: 'j',
    з: 'z',
    и: 'i',
    й: 'y',
    к: 'k',
    л: 'l',
    м: 'm',
    н: 'n',
    о: 'o',
    п: 'p',
    р: 'r',
    с: 's',
    т: 't',
    у: 'u',
    ф: 'f',
    х: 'h',
    ц: 'c',
    ч: 'ch',
    ш: 'sh',
    щ: 'shch',
    ы: 'y',
    ъ: 'y',
    ь: 'y',
    э: 'e',
    ю: 'u',
    я: 'ya',
    ' ': '_',
    ',': '_',
    '.': '_',
  };
  return word.split('').map((char) => keys[char] || char).join('');
};
